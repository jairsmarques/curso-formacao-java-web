
public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		// Operadores relacionais
		// Igual                   ->     ==
		// Diferente               ->     !=
		// Menor que               ->      <
		// Maior que               ->      >
		// Menor ou Igual que      ->     <=
		// Maior ou Igual que      ->     >=
		
		int nota1 = 91;
		int nota2 = 90;
		
		if (nota1 != nota2) {
			System.out.println("As notas são diferentes");
		} else {
			System.out.println("As notas são iguais");
		}

	}

}
