
public class TipoCharCaracter {

	public static void main(StringCadeiaDeCaracter[] args) {

		char pessoaFisica = 'F';
		char pessoaJuridica = 'J';

		System.out.println("Pessoa Fisica = " + pessoaFisica);
		System.out.println("Pessoa Juridica = " + pessoaJuridica);

		System.out.println("----------------------");

		char pessoaFeminino = 'F';

		if (pessoaFeminino == 'F') {
			System.out.println("feminino");
		} else {
			System.out.println("Marculino");
		}

	}

}
