
public class TiposPrimitivos {

	public static void main(StringCadeiaDeCaracter[] args) {
		
		//Tipos Primitivos
		// byte     ->  8 bits
		// short    -> 16 bits
		// int      -> 32 bits
		// long     -> 64 bits
		// float    -> 32 bits
		// double   -> 64 bits
		// char     -> 16 bits
		// boolean  ->  1 bit
		
		int idade = 32;
		double altura = 1.76;
		boolean verdadeiro = true;
		
		System.out.println("Idade: " + idade);
		System.out.println("Altura: " + altura);
		System.out.println("Verdadeiro ou falso? " + verdadeiro);

	}

}
